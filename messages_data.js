// Function to get random message from category
export function getRandomMessage(category) {
  const messages = messagesData[category];
  if (!messages || messages.length === 0) {
    return null;
  }
  const randomIndex = Math.floor(Math.random() * messages.length);
  return {
    id: `message_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`,
    text: messages[randomIndex],
    category: category,
    source: "Boomquotes Collection"
  };
}

// Function to get all messages from category
export function getCategoryMessages(category) {
  const messages = messagesData[category];
  if (!messages || messages.length === 0) {
    return [];
  }
  return messages.map((text, index) => ({
    id: `message_${Date.now()}_${category}_${index}`,
    text: text,
    category: category,
    source: "Boomquotes Collection"
  }));
}

// Function to get all messages
export function getAllMessages() {
  const allMessages = [];
  Object.keys(messagesData).forEach(category => {
    const categoryMessages = getCategoryMessages(category);
    allMessages.push(...categoryMessages);
  });
  return allMessages;
}

// Function to get daily message (deterministic based on date)
export function getDailyMessage() {
  const today = new Date();
  const dateString = today.toISOString().split('T')[0]; // YYYY-MM-DD format
  
  // Create a simple hash from the date string
  let hash = 0;
  for (let i = 0; i < dateString.length; i++) {
    const char = dateString.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash; // Convert to 32-bit integer
  }
  
  // Get all messages
  const allMessages = getAllMessages();
  if (allMessages.length === 0) return null;
  
  // Use hash to select a consistent message for today
  const index = Math.abs(hash) % allMessages.length;
  return allMessages[index];
}

